import UIKit

class LMColor: NSObject {
   
    class func navyColor() -> UIColor{
        return UIColor(red: 68/255, green: 115/255, blue: 180/255, alpha: 1)
    }
    
    class func orangeColor() -> UIColor{
        return UIColor(red: 255/255, green: 165/255, blue: 0/255, alpha: 1)
    }
    
    class func maroonColor() -> UIColor{
        return UIColor(red: 153/255, green: 15/255, blue: 96/255, alpha: 1)
    }
    class func greenColor() -> UIColor{
        return UIColor(red: 102/255, green: 204/255, blue: 51/255, alpha: 1)
    }
    
    class func coldBlueColor() -> UIColor{
        return UIColor(red: 183/255, green: 217/255, blue: 255/255, alpha: 1)
    }
    
    class func purpleColor() -> UIColor{
        return UIColor(red: 75/255, green: 52/255, blue: 99/255, alpha: 1)
    }
    
    class func darkPurpleColor() -> UIColor{
        return UIColor(red: 55/255, green: 41/255, blue: 71/255, alpha: 1)
    }
    
    class func lightPurpleColor() -> UIColor{
        return UIColor(red: 106/255, green: 72/255, blue: 141/255, alpha: 1)
    }
    
    class func grayedPurpleColor() -> UIColor{
        return UIColor(red: 126/255, green: 101/255, blue: 152/255, alpha: 1)
    }
}
